public class multiarreglo{
   public static void main(String args[]){

      int array[][]={{1,2,3},{2,23,3},{4,6,8}};
      
      
      
      for(int i = 0; i<array.length; i++){
         for(int j =0; j<array[0].length; j++){
         
            System.out.println(array[i][j]);         
         
        }      
      }
      

   }


}